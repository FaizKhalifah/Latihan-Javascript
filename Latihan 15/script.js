let pengurangan=(x,y)=>x-y;
let perkalian=(x,y)=>x*y;
let pembagian=(x,y)=>x/y;
document.write(pertambahan(9,5));
document.write("</br>");
document.write(pengurangan(18,11));
document.write("</br>");
document.write(perkalian(5,11));
